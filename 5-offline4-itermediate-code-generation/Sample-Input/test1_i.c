int main(){
 
	int i,j,k,ll,m,n,o,p;
 
	i = 1;
	println(i); // 1
	j = 5 + 8;
	println(j); // 13
	k = i + 2*j;
	println(k); // 27

	m = k%9;
	println(m); // 0
 
	n = m <= ll;
	println(n);
 
	o = i != j;
	println(o);
 
	p = n || o;
	println(p);
 
	p = n && o;
	println(p);
	
	p++;
	println(p);
 
	k = -p;
	println(k);
 
  
 
 
	return 0;
}

